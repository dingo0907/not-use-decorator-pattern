public class DarkRoast extends Beverage {

    public DarkRoast() {
        setDescription("DarkRoast");
        setMilk(true);
        setMocha(true);
        setSoy(true);
        setWhip(true);
    }

    public double cost(double DarkRoast_cost) {
        double cost = super.cost(DarkRoast_cost);
        return cost;
    }

}
